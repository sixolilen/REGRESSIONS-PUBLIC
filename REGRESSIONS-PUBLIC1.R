# Simple Linear Regression

# Importing the dataset
dataset = read.csv('Salary_Data.csv')

salary_data1 <- Salary_Data

#salary_data1 = salary_data1 [, 2;3]  specifying columns of interest 

# Splitting the dataset into the Training set and Test set
# install.packages('caTools')
library(caTools)
set.seed(123)
split = sample.split(salary_data1$Salary, SplitRatio = 2/3)
training_set = subset(salary_data1, split == TRUE)
test_set = subset(salary_data1, split == FALSE)

# Feature Scaling
# training_set = scale(training_set)
# test_set = scale(test_set)

# Fitting Simple Linear Regression to the Training set
regressor = lm(formula = Salary ~ YearsExperience,
               data = training_set)
#Summary(regressor) on console area to view results

# Predicting the Test set results
y_pred = predict(regressor, newdata = test_set)

#y_pred in Console

# Visualising the Training set results
library(tidyverse)
library(ggplot2)
ggplot() +
  geom_point(aes(x = training_set$YearsExperience, y = training_set$Salary),
             colour = 'red') +
  geom_line(aes(x = training_set$YearsExperience, y = predict(regressor, newdata = training_set)),
            colour = 'blue') +
  ggtitle('Salary vs Experience (Training set)') +
  xlab('Years of experience') +
  ylab('Salary')+
  labs(caption = "Source: UDEMY
                  Graphic: Sixolile Ngqwala at Good Governance Africa",
       title = "Salaries and the number of experience")

